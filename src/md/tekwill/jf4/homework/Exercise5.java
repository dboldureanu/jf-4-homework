package md.tekwill.jf4.homework;

public class Exercise5 {

    /*
     * Write a Java program to display the following pattern. Go to the editor
     * Sample Pattern :
     *
     *    J    a   v     v  a
     *    J   a a   v   v  a a
     * J  J  aaaaa   V V  aaaaa
     *  JJ  a     a   V  a     a
     */

    public static void main(String[] args) {
        String line_1 = "    J    a   v     v  a";
        String line_2 = "    J   a a   v   v  a a";
        String line_3 = " J  J  aaaaa   V V  aaaaa";
        String line_4 = "  JJ  a     a   V  a     a ";

        System.out.println(line_1);
        System.out.println(line_2);
        System.out.println(line_3);
        System.out.println(line_4);

    }
}
